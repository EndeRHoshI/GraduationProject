package com.hoshi.graduationproject.model;

public class Comments {
    private String comments_avatar;
    private String comments_nickname;
    private String comments_date;
    private String comments_content;

    public String getComments_avatar() {
        return comments_avatar;
    }

    public void setComments_avatar(String comments_avatar) {
        this.comments_avatar = comments_avatar;
    }

    public String getComments_nickname() {
        return comments_nickname;
    }

    public void setComments_nickname(String comments_nickname) {
        this.comments_nickname = comments_nickname;
    }

    public String getComments_date() {
        return comments_date;
    }

    public void setComments_date(String comments_date) {
        this.comments_date = comments_date;
    }

    public String getComments_content() {
        return comments_content;
    }

    public void setComments_content(String comments_content) {
        this.comments_content = comments_content;
    }
}
